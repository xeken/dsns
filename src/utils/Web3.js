export const getWeb3Provider = async () => {
    if (window.ethereum) {
        try {
            await window.ethereum.request({ method: 'eth_requestAccounts' });
            const provider = new ethers.BrowserProvider(window.ethereum);
            
            return provider;
        } catch (error) {
            console.error("User denied account access", error);
            throw error;
        }
    } else {
        throw new Error("MetaMask is not installed");
    }
};

export const getContract = async (provider) => {
    try {
        const signer = await provider.getSigner();
        
        const contract = new ethers.Contract(
            CONTRACT_ADDRESS,
            CONTRACT_ABI,
            signer
        );

        return contract;
    } catch (error) {
        console.error("Failed to get contract:", error);
        throw error;
    }
};

export const getUserInfo = async (provider) => {
    try {
        const address = (await provider.getSigner()).address;
        const balance = await provider.getBalance(address);
        const balanceEth = ethers.formatEther(balance);

        const profile = {}
        profile.address = address;
        profile.balance = balanceEth;

        return profile;
    } catch (err) {
        console.err(err);
    }

}

export const createJazzicon = (address) => {
    if (!address) throw new Error('Address is required to generate a Jazzicon');
    const numericSeed = parseInt(address.slice(2, 10), 16); 
    return jazzicon(40, numericSeed); 
  };